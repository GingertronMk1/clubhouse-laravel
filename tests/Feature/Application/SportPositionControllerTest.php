    public function test_edit(): void
    {
        $position = $this->sport->positions->first();
        $response = $this
            ->actingAs($this->user)
            ->get(route('sport.position.edit', ['sport' => $this->sport, 'position' => $position]));

        $response->assertStatus(200);
        $response->assertInertia(
            fn (AssertableInertia $page) => $page
                ->component('Position/Edit')
                ->has(
                    'sport',
                    fn (AssertableInertia $page) => $page
                        ->where('id', $this->sport->id)
                        ->etc(),
                )
        );
    }

    public function test_update(): void
    {
        $newName = 'test_update'.self::class;
        $position = $this->sport->positions->first();
        $response = $this
            ->actingAs($this->user)
            ->put(
                route(
                    'sport.position.update',
                    [
                        'sport' => $this->sport,
                        'position' => $position,
                    ],
                ),
                [
                    'name' => $newName,
                    'description' => null,
                ],
            );
        $response->assertRedirectToRoute('sport.position.index', ['sport' => $this->sport]);
        $position->refresh();
        $this->assertEquals($newName, $position->name);
    }

    public function test_destroy(): void
    {
        $position = $this->sport->positions->last();
        $this->assertNull($position->deleted_at);

        $response = $this->actingAs($this->user)->delete(route('sport.position.destroy', ['sport' => $this->sport, 'position' => $position]));
        $response->assertRedirectToRoute('sport.position.index', ['sport' => $this->sport]);
        $position->refresh();
        $this->assertNotNull($position->deleted_at);
    }
}
