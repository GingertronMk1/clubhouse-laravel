<?php

namespace Tests\Feature\Application;

use App\Models\Position;
use App\Models\Sport;
use Inertia\Testing\AssertableInertia;
use Tests\TestCase;

class SportPositionControllerTest extends TestCase
{
    protected Sport $sport;

    protected function setUp(): void
    {
        parent::setUp(); // TODO: Change the autogenerated stub
        $this->sport = Sport::factory()->has(Position::factory(5))->create();
    }

    public function test_index(): void
    {
        $response = $this->actingAs($this->user)->get(route('sport.position.index', ['sport' => $this->sport]));

        $response->assertStatus(200);
        $response->assertInertia(fn (AssertableInertia $page) => $page->component('Position/Index')
            ->has(
                'sport.positions',
                $this->sport->positions->count(),
            ));
    }

    public function test_create(): void
    {
        $response = $this
            ->actingAs($this->user)
            ->get(route('sport.position.create', ['sport' => $this->sport]));

        $response->assertStatus(200);
        $response->assertInertia(
            fn (AssertableInertia $page) => $page
                ->component('Position/Create')
                ->where('sport.id', $this->sport->id)
        );
    }

    public function test_store(): void
    {
        $testStoreName = 'test_store'.self::class;
        $previewPosition = 50;
        $response = $this->actingAs($this->user)->post(route('sport.position.store', ['sport' => $this->sport]), [
            'name' => $testStoreName,
            'preview_position_x' => $previewPosition,
            'preview_position_y' => $previewPosition,
        ]);
        $this->sport->refresh();
        $response->assertRedirectToRoute('sport.position.index', ['sport' => $this->sport]);
        $this->assertContains($testStoreName, $this->sport->positions->pluck('name'));
    }

